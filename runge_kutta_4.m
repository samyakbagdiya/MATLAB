function [y,yy] = runge_kutta_4(t0,tn,y0,z0,h)
a1 = 1/6.0;
a4 = 1/6.0; 
a2 = 2/6.0;
a3 = 2/6.0;
p1 = 1/2.0;
p2 = 1/2.0;
p3 = 1;
q11 = 1/2.0;
q21 = 0;
q22 = 1/2.0;
q31 = 0;
q32 = 0;
q33 = 1;
n = (tn-t0)/h +1;
while t0 <= tn
    k1(1)=h*fx(t0,y0,z0);
    k1(2)= h*fx(t0+p1*h,y0+k1(1)*q11,z0+k1(1)*q11);
    k1(3)=h*fx(t0+h*p2,y0+k1(2)*q22,z0+k1(2)*q22);
    k1(4) =h*fx(t0+h*p3,y0+k1(3)*q33,z0+k1(3)*q33);
    k2(1)=h*gx(t0,y0,z0);
    k2(2)= h*gx(t0+p1*h,y0+k2(1)*q11,z0+k2(1)*q11);
    k2(3)=h*gx(t0+h*p2,y0+k2(2)*q22,z0+k2(2)*q22);
    k2(4) =h*gx(t0+h*p3,y0+k2(3)*q33,z0+k2(3)*q33);
    y1=y0+ a1*k1(1)+ a2*k1(2)+ a3*k1(3)+ a4*k1(4);
    z1 = z0 + a1*k2(1)+ a2*k2(2) + a3*k2(3)+ a4*k2(4);
    t1 = t0+h;
    t0=t1;
    y0=y1;
    z0 = z1;

end
y = y0;
